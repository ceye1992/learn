//简单判断计算机是算术右移还是逻辑右移
//如果符号位为0（正数）则在右移的时候一定会移入0，但是当符号位为1，则算术会移入1， 逻辑会移入0

#include <stdio.h>

int main(void)
{
    unsigned w1 =0xF777EE22; //1111 0111 0111 0111 1110 1110 0010 0010
    int w2 =0xF777EE22;

    printf("%o\n", w1);

    w1 = w1 >>1;
    printf("逻辑右移为：%o\n", w1);  //逻辑右移
    w2 = w2 >>1;
    printf("计算机实际运行的结果：%o", w2); //如果一样则为逻辑右移，不一样则为算术右移



    return 0;
}
